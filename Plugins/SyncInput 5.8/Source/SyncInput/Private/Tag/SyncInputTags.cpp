FGameplayTag GetSyncInputNumberedTag(int32 Index)
{
	if (Index <= 0)
	{
		return FGameplayTag();
	}

	const FString TagString = FString::Printf(TEXT("SyncInput.%d"), Index);
	return UGameplayTagsManager::Get().RequestGameplayTag(FName(*TagString), /*ErrorIfNotFound*/ false);
}
